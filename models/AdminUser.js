const mongoose = require("mongoose");

const AdminUserSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true
  },
  email: {
    type: String,
    required: true,
    unique: true
  },
  password: {
    type: String,
    required: true
  },
  avatar: {
    type: String
  },
  date: {
    type: Date,
    default: Date.now
  }
});

//Export UserSchema
module.exports = AdminUser = mongoose.model("adminuser", AdminUserSchema);
